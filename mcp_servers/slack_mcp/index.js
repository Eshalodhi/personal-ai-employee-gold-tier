log(`Mode: Slack=${SIMULATE_SLACK ? "SIMULATION" : "LIVE"}`);
log(`Default channel: ${DEFAULT_CHANNEL} | Team channel: ${TEAM_CHANNEL}`);
log("Tools: send_channel_message, send_dm, post_file, get_mentions, create_reminder");
