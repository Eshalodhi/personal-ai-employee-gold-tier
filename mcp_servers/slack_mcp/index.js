const server = new Server(
  { name: "slack-mcp", version: "1.0.0" },
  { capabilities: { tools: {} } }
);

// ── Tool definitions ──────────────────────────────────────────

server.setRequestHandler(ListToolsRequestSchema, async () => ({
  tools: [
    {
      name: "send_channel_message",
      description: "Post a message to a Slack channel. Requires HITL approval before sending. Supports thread replies.",
      inputSchema: {
        type: "object",
        properties: {
          channel:   { type: "string",  description: "Channel name or ID (e.g. #general or C0123456789). Default: #general" },
          message:   { type: "string",  description: "Message text (supports Slack markdown)" },
          thread_ts: { type: "string",  description: "Thread timestamp to reply in a thread (optional)" },
          blocks:    { type: "array",   description: "Slack Block Kit blocks for rich formatting (optional)" },
        },
        required: ["message"],
      },
    },
    {
      name: "send_dm",
      description: "Send a direct message to a Slack user. Requires HITL approval before sending.",
      inputSchema: {
        type: "object",
        properties: {
          user_id: { type: "string", description: "Slack user ID (e.g. U0123456789) or username" },
          message: { type: "string", description: "Message text (supports Slack markdown)" },
        },
        required: ["user_id", "message"],
      },
    },
    {
      name: "post_file",
      description: "Upload a file from the vault to a Slack channel with an optional comment. Requires HITL approval.",
      inputSchema: {
        type: "object",
        properties: {
          channel:         { type: "string", description: "Channel to post to (default: #general)" },
          file_path:       { type: "string", description: "Path to file relative to vault root (e.g. Reports/CEO_Briefing_2026-02-23.md)" },
          title:           { type: "string", description: "File title in Slack (defaults to filename)" },
          initial_comment: { type: "string", description: "Message posted alongside the file (optional)" },
        },
        required: ["file_path"],
      },
    },
    {
      name: "get_mentions",
      description: "Get Slack messages where the AI Employee bot was @mentioned. Read-only — no HITL required.",
      inputSchema: {
        type: "object",
        properties: {
          channel:     { type: "string", description: "Limit to a specific channel (optional — default: all channels)" },
          max_results: { type: "number", description: "Maximum mentions to return (default: 10)" },
          since_ts:    { type: "string", description: "Only return mentions after this Slack timestamp (optional)" },
        },
      },
    },
    {
      name: "create_reminder",
      description: "Create a Slack reminder for a user. Requires HITL approval before creating.",
      inputSchema: {
        type: "object",
        properties: {
          text:    { type: "string", description: "Reminder message text" },
          time:    { type: "string", description: "When to send the reminder — ISO 8601 or relative (e.g. 'in 2 hours', 'tomorrow at 9am', '2026-03-01T09:00:00Z')" },
          user_id: { type: "string", description: "Slack user ID to remind (default: 'me' — the bot owner)" },
        },
        required: ["text", "time"],
      },
    },
  ],
}));

// ── Tool dispatcher ───────────────────────────────────────────

server.setRequestHandler(CallToolRequestSchema, async (request) => {
  const { name, arguments: args = {} } = request.params;
  log(`Tool called: ${name}`);
  try {
    switch (name) {
      case "send_channel_message": return await sendChannelMessage(args);
      case "send_dm":              return await sendDm(args);
      case "post_file":            return await postFile(args);
      case "get_mentions":         return await getMentions(args);
      case "create_reminder":      return await createReminder(args);
      default: return errorResult(`Unknown tool: ${name}`);
    }
  } catch (err) {
    log(`Error in tool ${name}: ${err.message}`);
    return errorResult(`Internal error: ${err.message}`);
  }
});

// ── Start ─────────────────────────────────────────────────────

const transport = new StdioServerTransport();
await server.connect(transport);

log("Slack MCP Server v1.0.0 started");
