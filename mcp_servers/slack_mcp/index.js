async function createReminder(args) {
  const {
    text,
    time,
    user_id = "me",
  } = args;

  if (!text) return errorResult("text is required — the reminder message");
  if (!time) return errorResult("time is required — ISO 8601 datetime or relative (e.g. 'in 1 hour', 'tomorrow at 9am')");

  log(`create_reminder: user=${user_id}, time="${time}", SIMULATE=${SIMULATE_SLACK}`);

  const reminderData = { text, time, user_id };

  if (SIMULATE_SLACK) {
    const { id, filename, filepath } = createSlackReminderApprovalFile(reminderData);
    return successResult({
      simulation: true,
      status: "pending_approval",
      reminder_id: id,
      reminder: { text, time, user_id },
      approval_file: filename,
      approval_path: filepath,
      message: `Slack reminder approval created. Move ${filename} to Approved/ to set.`,
      hitl_required: true,
    });
  }

  // PRODUCTION: POST https://slack.com/api/reminders.add
  // Params: text, time (Unix epoch or relative string), user
  return errorResult("LIVE mode not yet configured. Set SLACK_BOT_TOKEN and set SIMULATE_SLACK=false.");
}

