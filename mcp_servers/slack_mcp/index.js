async function sendChannelMessage(args) {
  const {
    channel = DEFAULT_CHANNEL,
    message,
    thread_ts = "",
    blocks = null,
  } = args;

  if (!message) return errorResult("message is required");

  log(`send_channel_message: channel=${channel}, SIMULATE=${SIMULATE_SLACK}`);

  const msgData = { channel, message, thread_ts, blocks };

  if (SIMULATE_SLACK) {
    const { id, filename, filepath } = createSlackMessageApprovalFile(msgData, "channel");
    return successResult({
      simulation: true,
      status: "pending_approval",
      message_id: id,
      target: { type: "channel", channel },
      preview: message.slice(0, 200),
      thread_ts: thread_ts || null,
      approval_file: filename,
      approval_path: filepath,
      message: `Slack message approval created. Move ${filename} to Approved/ to send.`,
      hitl_required: true,
    });
  }

  // PRODUCTION: POST https://slack.com/api/chat.postMessage
  return errorResult("LIVE mode not yet configured. Set SLACK_BOT_TOKEN and set SIMULATE_SLACK=false.");
}

