function createSlackMessageApprovalFile(msg, type = "channel") {
  ensureDir(path.join(VAULT_PATH, "Pending_Approval"));
  const id       = generateId();
  const prefix   = type === "dm" ? "SLACK_DM" : "SLACK_MESSAGE";
  const filename = `${prefix}_${id}_${todayStr()}.md`;
  const filepath = path.join(VAULT_PATH, "Pending_Approval", filename);
  const expires  = expiresIso(24);

  const targetLabel = type === "dm"
    ? `Direct Message to: ${msg.user_id}`
    : `Channel: ${msg.channel}`;

  const threadNote = msg.thread_ts
    ? `\n**Thread reply to:** ${msg.thread_ts}`
    : "";

  const content = `---
type: slack_${type}_approval
action: slack_send_${type === "dm" ? "dm" : "message"}
message_id: ${id}
target: ${type === "dm" ? msg.user_id : msg.channel}
created: ${nowIso()}
expires: ${expires}
status: pending
risk_level: medium
requested_by: slack_mcp
simulation_mode: ${SIMULATE_SLACK}
---

# Approval Request: Slack ${type === "dm" ? "Direct Message" : "Channel Message"}

**${targetLabel}**${threadNote}

## Message Preview

---

${msg.message}

---

## Details
- **Mode:** ${SIMULATE_SLACK ? "SIMULATION — no real Slack API call" : "LIVE — Slack Web API chat.postMessage"}
- **Expires:** ${expires}

## To Approve → move to Approved/
## To Reject  → move to Rejected/

---
*Generated by slack_mcp — AI Employee Vault Gold Tier*
`;

  fs.writeFileSync(filepath, content, "utf-8");
  log(`Slack message approval file created: ${filename}`);
  return { id, filename, filepath };
}

