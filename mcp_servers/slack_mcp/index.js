async function sendDm(args) {
  const {
    user_id,
    message,
  } = args;

  if (!user_id)  return errorResult("user_id is required (Slack user ID, e.g. U0123456789)");
  if (!message)  return errorResult("message is required");

  log(`send_dm: user=${user_id}, SIMULATE=${SIMULATE_SLACK}`);

  const msgData = { user_id, message };

  if (SIMULATE_SLACK) {
    const { id, filename, filepath } = createSlackMessageApprovalFile(msgData, "dm");
    return successResult({
      simulation: true,
      status: "pending_approval",
      message_id: id,
      target: { type: "dm", user_id },
      preview: message.slice(0, 200),
      approval_file: filename,
      approval_path: filepath,
      message: `Slack DM approval created. Move ${filename} to Approved/ to send.`,
      hitl_required: true,
    });
  }

  // PRODUCTION: open a DM channel first, then chat.postMessage
  // POST /api/conversations.open with users=[user_id]
  // Then POST /api/chat.postMessage with channel=dm_channel_id
  return errorResult("LIVE mode not yet configured. Set SLACK_BOT_TOKEN and set SIMULATE_SLACK=false.");
}

