async function postFile(args) {
  const {
    channel = DEFAULT_CHANNEL,
    file_path,
    title = "",
    initial_comment = "",
  } = args;

  if (!file_path) return errorResult("file_path is required (relative to vault root)");

  log(`post_file: file="${file_path}" → ${channel}, SIMULATE=${SIMULATE_SLACK}`);

  const uploadData = { channel, file_path, title: title || path.basename(file_path), initial_comment };

  if (SIMULATE_SLACK) {
    const { id, filename, filepath } = createSlackFileApprovalFile(uploadData);
    return successResult({
      simulation: true,
      status: "pending_approval",
      upload_id: id,
      target: { channel },
      file: {
        local_path: file_path,
        title: uploadData.title,
        initial_comment,
      },
      approval_file: filename,
      approval_path: filepath,
      message: `Slack file upload approval created. Move ${filename} to Approved/ to post.`,
      hitl_required: true,
    });
  }

  // PRODUCTION: POST https://slack.com/api/files.upload (multipart/form-data)
  return errorResult("LIVE mode not yet configured. Set SLACK_BOT_TOKEN and set SIMULATE_SLACK=false.");
}

